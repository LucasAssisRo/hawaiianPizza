import UIKit

extension UIColor {
    open class var shadowColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.2)
    }
    
    open class var titleColor: UIColor {
        return UIColor(red: 36/255, green: 63/255, blue: 133/255, alpha: 1.0)
    }
}
